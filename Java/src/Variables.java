public class Variables {
    public static void main (String [] args){
        int age = 21;
        String name = "Letícia";

        System.out.println("Olá! Me chamo " + name + " e tenho " + age + " anos de idade!");

        // Exemplos de tipos de variáveis
        byte byteVar = 127;
        short shortVar = 1000;
        int intVar = 10000;
        long longVar = 1000000L;
        float floatVar = 100.020F;
        double doubleVar = 100.000;
        boolean booVar = true;
        char charVar = 'A';

    }
}
